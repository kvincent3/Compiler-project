

public class Tableau {
	int dimension;
	int bounds[];
	 
	
	public Tableau(int dimension,int[] bounds){
		
		
		this.dimension=dimension;
		this.bounds=bounds;
	}


	public int getDimension() {
		return dimension;
	}


	public void setDimension(int dimension) {
		this.dimension = dimension;
	}


	public int[] getBounds() {
		return bounds;
	}


	public void setBounds(int[] bounds) {
		this.bounds = bounds;
	}
	

		
		
	}

